package com.fingarpay.helper;
public class MyCardInfo {

	private int Id;
	private String Username;
	private String MyCardName;
	private String BankName;
	private String MyCardNo;
	private String DateCreated;
	/**
	 * @return the id
	 */
	public int getId() {
		return Id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		Id = id;
	}
	/**
	 * @return the myCardName
	 */
	public String getMyCardName() {
		return MyCardName;
	}
	/**
	 * @param myCardName the myCardName to set
	 */
	public void setMyCardName(String myCardName) {
		MyCardName = myCardName;
	}
	/**
	 * @return the bankName
	 */
	public String getBankName() {
		return BankName;
	}
	/**
	 * @param bankName the bankName to set
	 */
	public void setBankName(String bankName) {
		BankName = bankName;
	}
	/**
	 * @return the myCardNo
	 */
	public String getMyCardNo() {
		return MyCardNo;
	}
	/**
	 * @param myCardNo the myCardNo to set
	 */
	public void setMyCardNo(String myCardNo) {
		MyCardNo = myCardNo;
	}
	/**
	 * @return the dateCreated
	 */
	public String getDateCreated() {
		return DateCreated;
	}
	/**
	 * @param dateCreated the dateCreated to set
	 */
	public void setDateCreated(String dateCreated) {
		DateCreated = dateCreated;
	}
	public String getUsername() {
		return Username;
	}
	public void setUsername(String username) {
		Username = username;
	}
	
	
}
