package com.fingarpay.helper;

public class FingerHelper {
	private byte[] RTfinger ;
	private byte[] RIfinger ;
	private byte[] LTfinger ;
	private byte[] LIfinger ;
	private String RegNo ;
	/**
	 * @return the rTfinger
	 */
	public byte[] getRTfinger() {
		return RTfinger;
	}
	/**
	 * @param rTfinger the rTfinger to set
	 */
	public void setRTfinger(byte[] rTfinger) {
		RTfinger = rTfinger;
	}
	/**
	 * @return the rIfinger
	 */
	public byte[] getRIfinger() {
		return RIfinger;
	}
	/**
	 * @param rIfinger the rIfinger to set
	 */
	public void setRIfinger(byte[] rIfinger) {
		RIfinger = rIfinger;
	}
	/**
	 * @return the lTfinger
	 */
	public byte[] getLTfinger() {
		return LTfinger;
	}
	/**
	 * @param lTfinger the lTfinger to set
	 */
	public void setLTfinger(byte[] lTfinger) {
		LTfinger = lTfinger;
	}
	/**
	 * @return the lIfinger
	 */
	public byte[] getLIfinger() {
		return LIfinger;
	}
	/**
	 * @param lIfinger the lIfinger to set
	 */
	public void setLIfinger(byte[] lIfinger) {
		LIfinger = lIfinger;
	}
	/**
	 * @return the regNo
	 */
	public String getRegNo() {
		return RegNo;
	}
	/**
	 * @param regNo the regNo to set
	 */
	public void setRegNo(String regNo) {
		RegNo = regNo;
	}
	
		
	
}
